# 입력을 받기위한 input 함수는 절대 사용하지 않습니다.
# 리스트 메서드 .count()를 사용하지 않습니다.
def find_solo(number_list):
    solo_dic = {}
    for i in number_list:
        if i not in solo_dic:
            solo_dic[i] = 1
        else:
            solo_dic[i] += 1
    solo_item = solo_dic.items()
    solo_num = 0
    for z in solo_item:
        if z[1] == 1:
            solo_num = z[0]
    return solo_num


    



